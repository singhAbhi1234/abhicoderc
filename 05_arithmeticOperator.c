#include <stdio.h>

int main()
{

  double first,second;
  printf("Enter your first number\n");
  scanf("%lf",&first);
  printf("Enter your second number\n");
  scanf("%lf",&second);
printf("the addition of first and second number is %lf\n",first+second);
printf("the subtraction of first and second number is %lf\n",first-second);
printf("the multiplication of first and second number is %lf\n",first*second);
printf("the division of first and second number is %lf\n",first/second);


}
